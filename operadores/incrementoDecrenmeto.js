let contador = 5;
console.log(++contador); // 6 (pre-incremento: suma antes de usar)
console.log(contador++); // 6 (post-incremento: usa antes de sumar)
console.log(contador);   // 7 (resultado final)

console.log(--contador); // 6 (pre-decremento: resta antes de usar)
console.log(contador--); // 6 (post-decremento: usa antes de restar)
console.log(contador);   // 5 (resultado final)
